package Week5.OOADLabWk5;

public class Specialisation extends Qualification {

	private String specialisationType;
	private String specialisationId;

	public String getSpecialisationType() {
		return this.specialisationType;
	}

	public void setSpecialisationType(String specialisationType) {
		this.specialisationType = specialisationType;
	}

	public String getSpecialisationId() {
		return this.specialisationId;
	}

	public void setSpecialisationId(String specialisationId) {
		this.specialisationId = specialisationId;
	}

}